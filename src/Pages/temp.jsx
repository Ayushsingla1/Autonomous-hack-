// Replace with your deployed contract address
const contractAddress = '0x494B0e287f24a1D3E0f89a5823D420705B9A5f84';

function App() {
  const [web3, setWeb3] = useState(null);
  const [contract, setContract] = useState(null);
  const [account, setAccount] = useState(null);
  const [events, setEvents] = useState([]);

  useEffect(() => {
    const init = async () => {
      if (window.ethereum) {
        try {
          // Request account access
          await window.ethereum.request({ method: 'eth_requestAccounts' });

          // Initialize Web3
          const newWeb3 = new Web3(window.ethereum);
          setWeb3(newWeb3);

          // Get the connected accounts
          const accounts = await newWeb3.eth.getAccounts();
          setAccount(accounts[0]);

          // Initialize the contract
          const newContract = new newWeb3.eth.Contract(EventTicketNFTABI, contractAddress);
          setContract(newContract);

          // Load events
          await loadEvents(newContract);
        } catch (error) {
          console.error("User denied account access or other error:", error);
        }
      } else {
        alert("Please install MetaMask!");
      }
    };

    init();
  }, []);

  // Function to create an event
  const createEvent = async (name, artistName, price, totalTickets) => {
    try {
      const tx = await contract.methods.createEvent(name, artistName, Web3.utils.toWei(price, 'ether'), totalTickets)
        .send({ from: account });
      console.log('Event created:', tx);
      await loadEvents(contract);
    } catch (error) {
      console.error('Error creating event:', error);
    }
  };

  // Function to buy a ticket
  const buyTicket = async (eventId, price) => {
    try {
      const tx = await contract.methods.buyTicket(eventId).send({
        from: account,
        value: Web3.utils.toWei(price, 'ether')
      });
      console.log('Ticket purchased:', tx);
    } catch (error) {
      console.error('Error buying ticket:', error);
    }
  };

  // Function to load events from the contract
  const loadEvents = async (contract) => {
    const eventCount = await contract.methods._tokenIds().call();
    let eventList = [];

    for (let i = 0; i < eventCount; i++) {
      const event = await contract.methods.events(i).call();
      eventList.push({
        name: event.name,
        artistName: event.artistName,
        price: Web3.utils.fromWei(event.price, 'ether'),
        availableTickets: event.availableTickets
      });
    }

    setEvents(eventList);
  };

  return (
    <div className="App">
      <h1>Event Ticket NFT</h1>
      <p>Connected Account: {account}</p>

      <h2>Create Event</h2>

      <button className="border-4" onClick={() => createEvent(data[0]["EventName"], data[0]["Artist"], data[0]["Price"], data[0]["TotalTickets"])}>
        Create Sample Event
      </button>

      <h2>Available Events</h2>
      {events.map((event, index) => (
        <div key={index}>
          <h3>{event.name} by {event.artistName}</h3>
          <p>Price: {event.price} ETH</p>
          <p>Tickets Available: {event.availableTickets}</p>
          <button onClick={() => buyTicket(index, event.price)}>
            Buy Ticket
          </button>
        </div>
      ))}
    </div>
  );
}

export default App;
